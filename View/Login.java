     private boolean checkLogin(String username , String password){
        String SQL = "Select * from admin_Login";
        String userData;
        String passData;
        try (
                Connection conn = ConnectorHelper.connection();
                Statement stm = conn.createStatement();
                ResultSet rs = stm.executeQuery(SQL);
                
                ){
            while (rs.next()) {
               userData = rs.getString("UserName");
               passData = rs.getString("Password");
                if ( username.equals(userData) && password.equals(passData)) {
                    System.out.println("Đăng nhập thành công");
                    return true;
                    
                }
            }
            System.out.println("Đăng nhập thất bại\n Chưa tạo email");
        } catch (SQLException ex) {
            System.out.println("Lỗi : "+ ex);
        }
        return false;
    }
